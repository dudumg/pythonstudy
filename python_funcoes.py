def divisao(dividendo, divisor):
    quociente = dividendo // divisor
    resto = dividendo % divisor
    print("Dividendo: ", dividendo)
    print("Divisor: ", divisor)
    print("Quociente: ", quociente)
    print("Resto: ", resto)


